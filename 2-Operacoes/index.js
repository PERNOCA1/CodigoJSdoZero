// OPERAÇÕES MATEMATICA
var resultado;
var x = 2;
var y = 5;

//soma
resultado = x + y;

console.log('Soma: ', resultado)

//subtracao
resultado = x - y;

console.log('Subtração: ', resultado)

//multiplicacao
resultado = x * y;

console.log('Multiplicacão: ', resultado)

//divisao
resultado = x / y;

console.log('Divisão: ', resultado)

// calculo do modulo (resto da divisao)
resultado = x % y;
console.log('Modulo: ', resultado)

